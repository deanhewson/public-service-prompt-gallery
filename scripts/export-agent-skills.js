/**
 * export-agent-skills.js
 *
 * Compiles each agent_export-enabled workflow into three agent-format
 * exports (Claude Skill, Copilot Agent Builder, generic agent instructions)
 * plus an index.json manifest, written to public/agent-skills/.
 *
 * Usage: node scripts/export-agent-skills.js
 */

import { readFileSync, writeFileSync, readdirSync, mkdirSync, existsSync } from 'fs';
import path from 'path';
import { fileURLToPath } from 'url';

const __dirname = path.dirname(fileURLToPath(import.meta.url));
const ROOT = path.join(__dirname, '..');
const WORKFLOWS_DIR = path.join(ROOT, 'data', 'workflows');
const BLOCKS_DIR = path.join(ROOT, 'data', 'building-blocks');
const GATES_DIR = path.join(ROOT, 'data', 'quality-gates');
const OUTPUT_DIR = path.join(ROOT, 'public', 'agent-skills');

const GENERATED_COMMENT = '<!-- Generated by scripts/export-agent-skills.js — do not edit. Edit the workflow JSON instead. -->';

const COPILOT_DESCRIPTION_LIMIT = 1000;
const COPILOT_INSTRUCTIONS_LIMIT = 8000;
const STEP_SUMMARY_CAP = 700;

const DOMAIN_LABELS = {
  policy_development: 'Policy Development',
  stakeholder_engagement: 'Stakeholder Engagement',
  general_productivity: 'General Productivity',
  communications: 'Communications',
  career_performance: 'Career & Performance',
};

// ---------------------------------------------------------------------------
// Data loading
// ---------------------------------------------------------------------------

function loadJsonDir(dir) {
  const map = {};
  for (const file of readdirSync(dir)) {
    if (!file.endsWith('.json')) continue;
    const data = JSON.parse(readFileSync(path.join(dir, file), 'utf8'));
    map[data.id] = data;
  }
  return map;
}

// ---------------------------------------------------------------------------
// Shared helpers
// ---------------------------------------------------------------------------

function humanize(s) {
  return s.replace(/_/g, ' ');
}

function maturityLabel(maturity) {
  return maturity;
}

function generationDate(workflow) {
  return workflow.maturity_updated || workflow.last_reviewed;
}

function resolveStepEntity(step, blocks, gates) {
  if (step.building_block_id) return blocks[step.building_block_id];
  if (step.quality_gate_id) return gates[step.quality_gate_id];
  return null;
}

function variablesAskLine(entity) {
  if (!entity.variables || entity.variables.length === 0) return null;
  const parts = entity.variables.map((v) => `${v.name} (${v.description})`);
  return `Ask the officer for any of the following details you don't yet have: ${parts.join(', ')}`;
}

function knownLimitationsBlock(entity, heading = 'Known limitations:') {
  if (!entity.known_limitations || entity.known_limitations.length === 0) return null;
  return [heading, ...entity.known_limitations.map((l) => `- ${l}`)].join('\n');
}

function toolAugmentedBlock(entity) {
  if (!entity.tool_augmented) return null;
  const ta = entity.tool_augmented;
  return [
    'If you have web search available, use the search-assisted version of this gate:',
    '',
    ta.prompt_template,
    '',
    `Note: ${ta.trust_note}`,
  ].join('\n');
}

function stripForCopilot(template) {
  return template
    .replace(/<[^>]+>/g, ' ')
    .replace(/\{\{[^}]+\}\}/g, '')
    .replace(/\s+/g, ' ')
    .trim();
}

// ---------------------------------------------------------------------------
// Main
// ---------------------------------------------------------------------------

function main() {
  const workflows = loadJsonDir(WORKFLOWS_DIR);
  const blocks = loadJsonDir(BLOCKS_DIR);
  const gates = loadJsonDir(GATES_DIR);

  if (!existsSync(OUTPUT_DIR)) mkdirSync(OUTPUT_DIR, { recursive: true });

  const enabled = Object.values(workflows).filter((wf) => wf.agent_export?.enabled === true);

  console.log(`=== APS Prompt Gallery — Agent Export Generator ===\n`);
  console.log(`Found ${enabled.length} workflow(s) with agent_export.enabled === true:`);
  for (const wf of enabled) {
    console.log(`  - ${wf.id} (${wf.slug}) — ${wf.steps.length} steps`);
  }
}

main();
