/**
 * export-agent-skills.js
 *
 * Compiles each agent_export-enabled workflow into three agent-format
 * exports (Claude Skill, Copilot Agent Builder, generic agent instructions)
 * plus an index.json manifest, written to public/agent-skills/.
 *
 * Usage: node scripts/export-agent-skills.js
 */

import { readFileSync, writeFileSync, readdirSync, mkdirSync, existsSync } from 'fs';
import path from 'path';
import { fileURLToPath } from 'url';

const __dirname = path.dirname(fileURLToPath(import.meta.url));
const ROOT = path.join(__dirname, '..');
const WORKFLOWS_DIR = path.join(ROOT, 'data', 'workflows');
const BLOCKS_DIR = path.join(ROOT, 'data', 'building-blocks');
const GATES_DIR = path.join(ROOT, 'data', 'quality-gates');
const OUTPUT_DIR = path.join(ROOT, 'public', 'agent-skills');

const GENERATED_COMMENT = '<!-- Generated by scripts/export-agent-skills.js — do not edit. Edit the workflow JSON instead. -->';

const COPILOT_DESCRIPTION_LIMIT = 1000;
const COPILOT_INSTRUCTIONS_LIMIT = 8000;
const STEP_SUMMARY_CAP = 700;

const DOMAIN_LABELS = {
  policy_development: 'Policy Development',
  stakeholder_engagement: 'Stakeholder Engagement',
  general_productivity: 'General Productivity',
  communications: 'Communications',
  career_performance: 'Career & Performance',
};

// ---------------------------------------------------------------------------
// Data loading
// ---------------------------------------------------------------------------

function loadJsonDir(dir) {
  const map = {};
  for (const file of readdirSync(dir)) {
    if (!file.endsWith('.json')) continue;
    const data = JSON.parse(readFileSync(path.join(dir, file), 'utf8'));
    map[data.id] = data;
  }
  return map;
}

// ---------------------------------------------------------------------------
// Shared helpers
// ---------------------------------------------------------------------------

function humanize(s) {
  return s.replace(/_/g, ' ');
}

function maturityLabel(maturity) {
  return maturity;
}

function generationDate(workflow) {
  return workflow.maturity_updated || workflow.last_reviewed;
}

function resolveStepEntity(step, blocks, gates) {
  if (step.building_block_id) return blocks[step.building_block_id];
  if (step.quality_gate_id) return gates[step.quality_gate_id];
  return null;
}

function variablesAskLine(entity) {
  if (!entity.variables || entity.variables.length === 0) return null;
  const parts = entity.variables.map((v) => `${v.name} (${v.description})`);
  return `Ask the officer for any of the following details you don't yet have: ${parts.join(', ')}`;
}

function knownLimitationsBlock(entity, heading = 'Known limitations:') {
  if (!entity.known_limitations || entity.known_limitations.length === 0) return null;
  return [heading, ...entity.known_limitations.map((l) => `- ${l}`)].join('\n');
}

function toolAugmentedBlock(entity) {
  if (!entity.tool_augmented) return null;
  const ta = entity.tool_augmented;
  return [
    'If you have web search available, use the search-assisted version of this gate:',
    '',
    ta.prompt_template,
    '',
    `Note: ${ta.trust_note}`,
  ].join('\n');
}

function stripForCopilot(template) {
  return template
    .replace(/<[^>]+>/g, ' ')
    .replace(/\{\{[^}]+\}\}/g, '')
    .replace(/\s+/g, ' ')
    .trim();
}

// ---------------------------------------------------------------------------
// Claude Skill (2a)
// ---------------------------------------------------------------------------

function buildClaudeSkill(workflow, blocks, gates) {
  const skillName = `aps-${workflow.slug}`;
  const taskType = humanize(workflow.task_type || workflow.name).toLowerCase();
  const description = `${workflow.description} Follows the APS Prompt Gallery ${workflow.name} methodology with mandatory officer checkpoints. Use when an Australian Public Service officer asks for help producing ${taskType}.`;

  const lines = [];
  lines.push('---');
  lines.push(`name: ${skillName}`);
  lines.push(`description: ${description}`);
  lines.push('---');
  lines.push('');
  lines.push(GENERATED_COMMENT);
  lines.push('');
  lines.push(`# ${workflow.name} — APS Prompt Gallery methodology`);
  lines.push('');
  lines.push(`**EXPERIMENTAL.** This skill is generated from the APS Prompt Gallery's ${workflow.name} workflow (maturity: ${maturityLabel(workflow.maturity)}). It has not yet been tested in real agent deployments. The officer using it remains the accountable officer for any output under the PGPA Act.`);
  lines.push('');
  lines.push('## How to run this workflow');
  lines.push('');
  lines.push('Work through the steps below **in order, one step per response**. Never combine steps. Never skip a step marked CHECKPOINT.');
  lines.push('');
  lines.push('At every step:');
  lines.push("- Produce the step's output in full — never summarise your own interim work.");
  lines.push('- End your response with: `— END OF STEP {n}: {step name}. Before continuing: {step.user_action_after}`');
  lines.push('- At steps marked **CHECKPOINT**, additionally state: "This is an officer checkpoint. I will not continue until you confirm you have reviewed the output above." Then stop and wait. Do not proceed on an ambiguous reply — ask for explicit confirmation.');
  lines.push('');
  lines.push("Trust guidance: steps are marked high / medium / low trust. Restate the step's trust level when you present its output. Never present low-trust content (specific statistics, legislative references, program details) as settled fact.");
  lines.push('');
  lines.push("Classification: follow the officer's agency guidance on what information classification may be used with this tool. Do not encourage the officer to paste material above the approved classification.");
  lines.push('');
  lines.push('## Steps');
  lines.push('');

  for (const step of workflow.steps) {
    const entity = resolveStepEntity(step, blocks, gates);
    const isCheckpoint = step.agent_mode === 'human_gate';
    const isOptional = step.is_optional === true;

    let heading = `### Step ${step.order}: ${step.name}`;
    if (isCheckpoint) heading += ' — CHECKPOINT';
    if (isOptional) heading += ' (optional in manual use — included by default here)';
    lines.push(heading);
    lines.push(`Trust level: ${step.trust_level}.`);
    lines.push(step.description);
    lines.push('');
    lines.push(`When you finish this step, the officer's next action is: ${step.user_action_after}`);
    lines.push('');
    lines.push('Prompt methodology for this step:');
    lines.push('');

    const askLine = variablesAskLine(entity);
    if (askLine) {
      lines.push(askLine);
      lines.push('');
    }
    lines.push(entity.prompt_template);

    const toolBlock = toolAugmentedBlock(entity);
    if (toolBlock) {
      lines.push('');
      lines.push(toolBlock);
    }

    const limitations = knownLimitationsBlock(entity);
    if (limitations) {
      lines.push('');
      lines.push(limitations);
    }

    lines.push('');
  }

  lines.push('## Appendix: workflow limitations');
  lines.push('');
  for (const lim of workflow.known_limitations) {
    lines.push(`- ${lim}`);
  }
  lines.push('');
  lines.push(`Generated from the APS Prompt Gallery (public-service-prompt-gallery.netlify.app), ${generationDate(workflow)}.`);

  return lines.join('\n') + '\n';
}

// ---------------------------------------------------------------------------
// Shared ROLE/PROTOCOL/CLOSING/STEP sections (generic + Copilot)
// ---------------------------------------------------------------------------

function buildRoleSection(workflow) {
  return [
    '# ROLE',
    `You guide APS officers through the ${workflow.name} workflow, one step per turn. You are EXPERIMENTAL and untested in production; say so if asked. The officer remains the accountable officer under the PGPA Act.`,
  ].join('\n');
}

function buildProtocolSection() {
  return [
    '# NON-NEGOTIABLE PROTOCOL',
    '- Exactly one workflow step per turn. Never combine or skip steps.',
    '- Show all interim work in full. Never silently chain steps or summarise your own previous output instead of showing it.',
    '- End every turn with: "END OF STEP {n}: {name}. Before continuing: {what the officer should check}."',
    '- Steps marked CHECKPOINT: after presenting the output, say "Officer checkpoint — reply \'confirmed\' once you have reviewed the above." Do not continue for any other reply; if the user asks to skip, explain the checkpoint exists for PGPA accountability and ask again.',
    "- Restate each step's trust level (high/medium/low) with its output. Never present statistics, legislative references, or program details as settled fact — mark them [VERIFY].",
    '- Never fabricate figures, names, dates, or program details; use [INSERT DATA] placeholders.',
    '- Follow agency guidance on information classification.',
  ].join('\n');
}

function buildClosingSection(workflow) {
  return [
    '# CLOSING',
    `After the final step, list every unresolved [VERIFY] and [INSERT DATA] item, restate that the output is an AI-assisted draft requiring officer review, and point to public-service-prompt-gallery.netlify.app/workflows/${workflow.slug} for the full methodology.`,
  ].join('\n');
}

function buildStepSection(step, entity, headingPrefix) {
  const isCheckpoint = step.agent_mode === 'human_gate';
  const isOptional = step.is_optional === true;

  const lines = [];
  let heading = `${headingPrefix} ${step.order}: ${step.name}`;
  if (isCheckpoint) heading += ' — CHECKPOINT';
  if (isOptional) heading += ' (optional in manual use — included by default here)';
  lines.push(heading);
  lines.push(`Trust level: ${step.trust_level}.`);
  lines.push(step.description);
  lines.push('');
  lines.push(`When you finish this step, the officer's next action is: ${step.user_action_after}`);
  lines.push('');

  const askLine = variablesAskLine(entity);
  if (askLine) {
    lines.push(askLine);
    lines.push('');
  }
  lines.push(entity.prompt_template);

  const toolBlock = toolAugmentedBlock(entity);
  if (toolBlock) {
    lines.push('');
    lines.push(toolBlock);
  }

  const limitations = knownLimitationsBlock(entity);
  if (limitations) {
    lines.push('');
    lines.push(limitations);
  }

  return lines.join('\n');
}

// ---------------------------------------------------------------------------
// Generic agent instructions (2c)
// ---------------------------------------------------------------------------

function buildGenericInstructions(workflow, blocks, gates) {
  const lines = [];
  lines.push(GENERATED_COMMENT);
  lines.push('');
  lines.push(`**EXPERIMENTAL.** These instructions implement the APS Prompt Gallery's ${workflow.name} workflow (maturity: ${maturityLabel(workflow.maturity)}) for a generic agent platform. They have not yet been tested in real agent deployments. The officer using this agent remains the accountable officer for any output under the PGPA Act. Enforcement of the checkpoints below depends on the agent platform you are using — some platforms may not reliably pause for confirmation. The officer must sight and review the output at every CHECKPOINT step regardless of what the agent reports.`);
  lines.push('');
  lines.push(buildRoleSection(workflow));
  lines.push('');
  lines.push(buildProtocolSection());
  lines.push('');
  lines.push('# STEPS');
  lines.push('');

  for (const step of workflow.steps) {
    const entity = resolveStepEntity(step, blocks, gates);
    lines.push(buildStepSection(step, entity, '##'));
    lines.push('');
  }

  lines.push(buildClosingSection(workflow));

  return lines.join('\n') + '\n';
}

// ---------------------------------------------------------------------------
// Copilot Agent Builder (2b)
// ---------------------------------------------------------------------------

function copilotStepSummary(step, entity) {
  const core = stripForCopilot(entity.prompt_template);
  let combined = `${step.description} ${core}`.replace(/\s+/g, ' ').trim();
  if (combined.length > STEP_SUMMARY_CAP) {
    combined = combined.slice(0, STEP_SUMMARY_CAP - 1).trimEnd() + '…';
  }
  return combined;
}

function buildCopilotAgent(workflow, blocks, gates) {
  const oneLineSummary = workflow.description.replace(/\.\s*$/, '');
  const taskType = humanize(workflow.task_type || workflow.name).toLowerCase();
  const domainLabel = (DOMAIN_LABELS[workflow.domain] || humanize(workflow.domain)).toLowerCase();

  const descriptionField = `Guides Australian Public Service officers through the APS Prompt Gallery's ${workflow.name} workflow: ${oneLineSummary}. Works step by step with mandatory officer checkpoints for verification and compliance reflection. Use when drafting ${taskType} or when asked for structured, accountable AI assistance with ${domainLabel}.`;

  const stepLines = workflow.steps.map((step) => {
    const entity = resolveStepEntity(step, blocks, gates);
    const isCheckpoint = step.agent_mode === 'human_gate';
    const checkpointTag = isCheckpoint ? ' — CHECKPOINT' : '';
    const summary = copilotStepSummary(step, entity);
    return `${step.order}. ${step.name}${checkpointTag} (trust: ${step.trust_level}). ${summary}`;
  });

  const instructionsField = [
    buildRoleSection(workflow),
    '',
    buildProtocolSection(),
    '',
    '# STEPS',
    '',
    ...stepLines,
    '',
    buildClosingSection(workflow),
  ].join('\n');

  const lines = [];
  lines.push(GENERATED_COMMENT);
  lines.push('');
  lines.push(`# ${workflow.name} — Copilot Agent Builder configuration`);
  lines.push('<!-- Paste each block into the matching Agent Builder field. -->');
  lines.push('');
  lines.push('## Description field (max 1,000 characters)');
  lines.push('');
  lines.push(descriptionField);
  lines.push('');
  lines.push('## Instructions field (max 8,000 characters)');
  lines.push('');
  lines.push(instructionsField);

  return {
    markdown: lines.join('\n') + '\n',
    descriptionField,
    instructionsField,
  };
}

// ---------------------------------------------------------------------------
// Main
// ---------------------------------------------------------------------------

function main() {
  const workflows = loadJsonDir(WORKFLOWS_DIR);
  const blocks = loadJsonDir(BLOCKS_DIR);
  const gates = loadJsonDir(GATES_DIR);

  if (!existsSync(OUTPUT_DIR)) mkdirSync(OUTPUT_DIR, { recursive: true });

  const enabled = Object.values(workflows).filter((wf) => wf.agent_export?.enabled === true);

  console.log(`=== APS Prompt Gallery — Agent Export Generator ===\n`);
  console.log(`Found ${enabled.length} workflow(s) with agent_export.enabled === true.\n`);

  const charErrors = [];
  const indexEntries = [];

  for (const wf of enabled) {
    const claudeSkillMd = buildClaudeSkill(wf, blocks, gates);
    writeFileSync(path.join(OUTPUT_DIR, `${wf.slug}-claude-skill.md`), claudeSkillMd, 'utf8');

    const genericMd = buildGenericInstructions(wf, blocks, gates);
    writeFileSync(path.join(OUTPUT_DIR, `${wf.slug}-agent-instructions.md`), genericMd, 'utf8');

    const { markdown: copilotMd, descriptionField, instructionsField } = buildCopilotAgent(wf, blocks, gates);
    writeFileSync(path.join(OUTPUT_DIR, `${wf.slug}-copilot-agent.md`), copilotMd, 'utf8');

    console.log(`${wf.id} (${wf.slug}) — ${wf.steps.length} steps`);
    console.log(`  copilot description: ${descriptionField.length}/${COPILOT_DESCRIPTION_LIMIT} chars`);
    console.log(`  copilot instructions: ${instructionsField.length}/${COPILOT_INSTRUCTIONS_LIMIT} chars`);

    if (descriptionField.length > COPILOT_DESCRIPTION_LIMIT) {
      charErrors.push(`${wf.id}: Copilot description field is ${descriptionField.length} chars, exceeds limit of ${COPILOT_DESCRIPTION_LIMIT} by ${descriptionField.length - COPILOT_DESCRIPTION_LIMIT}`);
    }
    if (instructionsField.length > COPILOT_INSTRUCTIONS_LIMIT) {
      charErrors.push(`${wf.id}: Copilot instructions field is ${instructionsField.length} chars, exceeds limit of ${COPILOT_INSTRUCTIONS_LIMIT} by ${instructionsField.length - COPILOT_INSTRUCTIONS_LIMIT}`);
    }

    indexEntries.push({
      slug: wf.slug,
      name: wf.name,
      files: {
        claude_skill: `${wf.slug}-claude-skill.md`,
        copilot_agent: `${wf.slug}-copilot-agent.md`,
        generic: `${wf.slug}-agent-instructions.md`,
      },
      experimental: wf.agent_export.platforms,
    });
  }

  writeFileSync(path.join(OUTPUT_DIR, 'index.json'), JSON.stringify(indexEntries, null, 2) + '\n', 'utf8');
  console.log(`\nWrote index.json (${indexEntries.length} workflows)`);

  if (charErrors.length > 0) {
    console.error('\nCharacter limit violations:');
    for (const e of charErrors) console.error(`  ${e}`);
    process.exit(1);
  }

  console.log('\nDone.');
}

main();
